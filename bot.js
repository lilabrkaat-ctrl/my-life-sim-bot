// bot.js - فایل اصلی ربات شبیه‌ساز ریاست جمهوری ایران

const { Bot } = require("grammy");
const { TOKEN } = require("./config");
const { setupHandlers } = require("./handlers");

// ============================================
// 🚀 راه‌اندازی ربات
// ============================================

// بررسی توکن
if (!TOKEN || TOKEN === "TOKEN_DEFAULT") {
    console.error("❌ خطا: توکن ربات تنظیم نشده است!");
    console.error("💡 لطفاً متغیر BOT_TOKEN را در Railway تنظیم کنید.");
    process.exit(1);
}

// ساخت ربات
const bot = new Bot(TOKEN);

// ============================================
// 📝 تنظیم هندلرها
// ============================================

setupHandlers(bot);

// ============================================
// 🛡️ مدیریت خطاها
// ============================================

bot.catch((err) => {
    const ctx = err.ctx;
    console.error(`❌ خطا در پردازش پیام از ${ctx?.from?.id}:`, err.error);
    
    // پاسخ به کاربر در صورت امکان
    if (ctx?.callbackQuery) {
        ctx.answerCallbackQuery("⚠️ خطایی رخ داد. لطفاً دوباره تلاش کنید.").catch(() => {});
    }
});

// ============================================
// 📊 نمایش اطلاعات راه‌اندازی
// ============================================

console.log("🏛️  ═══════════════════════════════");
console.log("🇮🇷  ربات شبیه‌ساز ریاست جمهوری ایران");
console.log("═══════════════════════════════");
console.log("📅  شروع: ۱۴۰۵");
console.log("🌍  ۵۲ کشور قابل تعامل");
console.log("⚔️   جنگ • مذاکره • تجارت");
console.log("🏴  گروه‌های نیابتی");
console.log("⚛️  برنامه هسته‌ای");
console.log("💰  اقتصاد پیشرفته");
console.log("═══════════════════════════════");
console.log("✅  ربات آماده به کار است!");
console.log("🤖  در تلگرام /start را بزنید");
console.log("═══════════════════════════════\n");

// ============================================
// 🎮 شروع ربات
// ============================================

bot.start({
    onStart: (botInfo) => {
        console.log(`✅ @${botInfo.username} با موفقیت راه‌اندازی شد!`);
    }
});

// ============================================
// 🔄 مدیریت خاموشی
// ============================================

process.on('SIGINT', () => {
    console.log('\n🛑 ربات در حال خاموش شدن...');
    bot.stop();
    process.exit(0);
});

process.on('SIGTERM', () => {
    console.log('\n🛑 ربات در حال خاموش شدن...');
    bot.stop();
    process.exit(0);
});