function getDialogue(char1, char2, type) {
    const key1 = char1 + '_' + char2;
    const key2 = char2 + '_' + char1;
    
    if (dialogues[type] && dialogues[type][key1]) {
        return dialogues[type][key1];
    }
    if (dialogues[type] && dialogues[type][key2]) {
        return dialogues[type][key2];
    }
    
    return dialogues[type] ? dialogues[type].default : ['🎭 "اتفاقی در حال رخ دادنه..."'];
}

